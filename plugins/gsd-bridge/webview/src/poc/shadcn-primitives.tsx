import { twMerge } from 'tailwind-merge';

// =====================================================================
// Inline shadcn/ui primitives for the POC (Plan 04-05 v2 refactor).
//
// The primitives (Card, CardHeader, CardTitle, CardDescription, CardContent,
// RadioGroup, RadioGroupItem, Checkbox, Label, Separator) are the stock
// shadcn-ui React components — same source, same Tailwind classes, same
// Radix UI primitives — copied inline because `@json-render/shadcn` only
// exposes them through its json-render adapter layer (expecting
// `{ props, emit, bindings }` shape), which is unsuitable for the POC's
// direct-React interactive flow (the user must click a radio and see state
// update).
//
// These components are POC-scoped — host-injected specs continue to render
// through `@json-render/shadcn`'s json-render-driven adapters via the
// registry (`src/registry.ts`). The POC exists only as the no-host fallback
// in `app.tsx`.
// =====================================================================

function cn(...inputs: Array<string | false | null | undefined>): string {
  return twMerge(clsx(inputs));
}

